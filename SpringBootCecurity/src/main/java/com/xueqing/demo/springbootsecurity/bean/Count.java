package com.xueqing.demo.springbootsecurity.bean;
public class Count {
    private int id;
    private int dt_count;
    private int qp_count;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getDt_count() {
        return dt_count;
    }

    public void setDt_count(int dt_count) {
        this.dt_count = dt_count;
    }

    public int getQp_count() {
        return qp_count;
    }

    public void setQp_count(int qp_count) {
        this.qp_count = qp_count;
    }

    @Override
    public String toString() {
        return "Count{" +
                "id=" + id +
                ", dt_count=" + dt_count +
                ", qp_count=" + qp_count +
                '}';
    }
}
