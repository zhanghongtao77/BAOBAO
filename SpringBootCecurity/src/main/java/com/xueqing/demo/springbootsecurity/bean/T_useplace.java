package com.xueqing.demo.springbootsecurity.bean;

public class T_useplace {

    private int csid;
    private int cscode;
    private String csname;
    private String csaddress;
    private int cscapacity;
    private String equipment_type;
    private String cssite_type;

    public int getCsid() {
        return csid;
    }

    public void setCsid(int csid) {
        this.csid = csid;
    }

    public int getCscode() {
        return cscode;
    }

    public void setCscode(int cscode) {
        this.cscode = cscode;
    }

    public String getCsname() {
        return csname;
    }

    public void setCsname(String csname) {
        this.csname = csname;
    }

    public String getCsaddress() {
        return csaddress;
    }

    public void setCsaddress(String csaddress) {
        this.csaddress = csaddress;
    }

    public int getCscapacity() {
        return cscapacity;
    }

    public void setCscapacity(int cscapacity) {
        this.cscapacity = cscapacity;
    }

    public String getEquipment_type() {
        return equipment_type;
    }

    public void setEquipment_type(String equipment_type) {
        this.equipment_type = equipment_type;
    }

    public String getCssite_type() {
        return cssite_type;
    }

    public void setCssite_type(String cssite_type) {
        this.cssite_type = cssite_type;
    }

    @Override
    public String toString() {
        return "T_useplace{" +
                "csid=" + csid +
                ", cscode=" + cscode +
                ", csname='" + csname + '\'' +
                ", csaddress='" + csaddress + '\'' +
                ", cscapacity=" + cscapacity +
                ", equipment_type='" + equipment_type + '\'' +
                ", cssite_type='" + cssite_type + '\'' +
                '}';
    }
}
