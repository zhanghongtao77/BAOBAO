package com.xueqing.demo.springbootsecurity.bean;

public class T_useunit {

    private int id;
    private int unit_id;
    private String unit_name;
    private String unit_address;
    private String unit_contact;
    private String unit_duty;
    private String unit_duty_name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUnit_id() {
        return unit_id;
    }

    public void setUnit_id(int unit_id) {
        this.unit_id = unit_id;
    }

    public String getUnit_name() {
        return unit_name;
    }

    public void setUnit_name(String unit_name) {
        this.unit_name = unit_name;
    }

    public String getUnit_address() {
        return unit_address;
    }

    public void setUnit_address(String unit_address) {
        this.unit_address = unit_address;
    }

    public String getUnit_contact() {
        return unit_contact;
    }

    public void setUnit_contact(String unit_contact) {
        this.unit_contact = unit_contact;
    }

    public String getUnit_duty() {
        return unit_duty;
    }

    public void setUnit_duty(String unit_duty) {
        this.unit_duty = unit_duty;
    }

    public String getUnit_duty_name() {
        return unit_duty_name;
    }

    public void setUnit_duty_name(String unit_duty_name) {
        this.unit_duty_name = unit_duty_name;
    }

    @Override
    public String toString() {
        return "T_useunit{" +
                "id=" + id +
                ", unit_id=" + unit_id +
                ", unit_name='" + unit_name + '\'' +
                ", unit_address='" + unit_address + '\'' +
                ", unit_contact='" + unit_contact + '\'' +
                ", unit_duty='" + unit_duty + '\'' +
                ", unit_duty_name='" + unit_duty_name + '\'' +
                '}';
    }
}
