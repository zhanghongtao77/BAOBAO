package com.xueqing.demo.springbootsecurity.bean;

public class T_sosshbie {

    private int shibieid;
    private String shibiename;

    public int getShibieid() {
        return shibieid;
    }

    public void setShibieid(int shibieid) {
        this.shibieid = shibieid;
    }

    public String getShibiename() {
        return shibiename;
    }

    public void setShibiename(String shibiename) {
        this.shibiename = shibiename;
    }

    @Override
    public String toString() {
        return "T_sosshbie{" +
                "shibieid=" + shibieid +
                ", shibiename='" + shibiename + '\'' +
                '}';
    }
}
